import SwiftUI

enum AppColors {

    // Screen + Cards
    static let screenBg = Color(.systemGroupedBackground)
    static let cardBg = Color(.secondarySystemBackground)

    // Icon bg
    static let iconBg = Color(.secondarySystemBackground)
    static let iconFg = Color(.gray)

    // Main colors
    static let primaryBlue = Color.blue
    static let primaryGreen = Color.green
    static let primaryOrange = Color.orange

    // Soft backgrounds (looks good in dark + light)
    static let blueSoftBg = Color.blue.opacity(0.20)
    static let greenSoftBg = Color.green.opacity(0.20)
    static let orangeSoftBg = Color.orange.opacity(0.20)
}
